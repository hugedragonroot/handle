//LCD 模块初始化
void lcd_init(void)
{
	LCD_RST_OFF(); //低电平复位
	delay_ms(100);
	LCD_RST_ON(); //复位完毕
	delay_ms(100);
	transfer_command(0xe2); //软复位
	delay_ms(50);
	transfer_command(0x2c); //软复位
	delay_ms(50);
	transfer_command(0x2e); //软复位	
	delay_ms(50);
	transfer_command(0x2f); //打开内部升压
	delay_ms(500);
	transfer_command(0x24);  /*粗调对比度，可设置范围0x20～0x27*/
	transfer_command(0x81);  /*微调对比度*/
	transfer_command(0x1b);  /*0x1a,微调对比度的值，可设置范围0x00～0x3f*/
	transfer_command(0xa2);  /*1/9偏压比（bias）*/
	transfer_command(0xc0);  /*行扫描顺序：从上到下*/
	transfer_command(0xa1);  /*列扫描顺序：从左到右*/
	transfer_command(0x40);  /*起始行：第一行开始*/
	transfer_command(0xaf);  /*开显示*/
	clear_screen();

	
}

void lcd_address(uint8_t page,uint8_t column)
{
	column=column-1; //我们平常所说的第1列，在LCD驱动IC里是第0列。所以在这里减去1.
	page=page-1;
	transfer_command(0xb0+page); //设置页地址。每页8行。一个画面的64行被分成8个页。平常所说的第1页，在LCD驱动IC里是第0页，所以减去1
	transfer_command(((column>>4)&0x0f)+0x10); //设置列地址的高 4 位
	transfer_command(column&0x0f); //设置列地址的低 4 位
}



//更新显存到LCD		 
void lcd_refreshGram(void)
{
	uint8_t i,j;
	for(i=0;i<(LCD_HIGH/8);i++)
	{
		lcd_address(1+i,1);
		for(j=0;j<LCD_WIDTH;j++)
		{
			transfer_data(lcdGram[j][i]);
		}
	}	
}


//全屏清屏
void lcd_clear(void)
{
	uint8_t i,j;
	for(i=0;i<(LCD_HIGH/8);i++)
	{
//		lcd_address(1+i,1);
		for(j=0;j<LCD_WIDTH;j++)
		{
			lcdGram[j][i]=0x00;
//			transfer_data(0x00);
		}
	}
}



//画点 
//x:0~191
//y:0~63
//t:1 填充 0,清空				   
void lcd_drawPoint(u8 x,u8 y,u8 t)
{
	u8 pos,bx,temp=0;
	if(x>LCD_WIDTH-1||y>LCD_HIGH-1)return;//超出范围了.
	pos=y/8;
	bx=y%8;
	temp=1<<(bx);
	if(t)lcdGram[x][pos]|=temp;
	else lcdGram[x][pos]&=~temp;	    
}
//读点 
//x:0~191
//y:0~63
u8 lcd_readPoint(u8 x,u8 y)
{
	u8 pos,bx,temp=0x00;
	//x = LCD_WIDTH-1-x;
	y = LCD_HIGH-1-y;
	pos=y/8;
	bx=y%8;
	temp=1<<bx;
  if(temp&lcdGram[x][pos]) return 1;
	return 0;
}
//x1,y1,x2,y2 填充区域的对角坐标
//确保x1<=x2;y1<=y2 0<=x1<=127 0<=y1<=63	 	 
//dot:0,清空;1,填充	  
void lcd_fill(u8 x1,u8 y1,u8 x2,u8 y2,u8 dot)  
{  
	u8 x,y;  
	for(x=x1;x<=x2;x++)
		for(y=y1;y<=y2;y++)
			lcd_drawPoint(x,y,dot);											    
//	oled_refreshGram();//更新显示
}
//在指定位置显示一个字符,包括部分字符
//x:0~191
//y:0~63
//f_w:字宽
//f_h:字高
//mode:1,反白显示;0,正常显示				 
void lcd_showChar(u8 x,u8 y,u8 chr,u8 f_w,u8 f_h,u8 mode)
{      			    
	u8 temp,t,t1,n;

	u8 csize = f_h/8;
	
	chr=chr-' ';//得到偏移后的值		 

	for(n=0;n<csize;n++)
	{
		for(t=0;t<f_w;t++)
		{   
			if(f_w==5&&f_h==8)temp=ascii_table_5x8[chr][t+f_w*n];		//调用0508ascii字体
			else if(f_w==8&&f_h==16)temp=ascii_table_8x16[chr][t+f_w*n];	//调用0816ascii字体
//		else if(f_w==12&&f_h==24)temp=asc2_1224[chr][t];//调用1224ascii字体
			else return;	//没有的字库
			for(t1=0;t1<8;t1++)
			{
				if(temp&0x01)
					lcd_drawPoint(x,y,!mode);
				else 
					lcd_drawPoint(x,y,mode);
				temp >>=1;
				y++;
			} 
			y=y-8;
			x++;			
		}
		x = x - f_w;
		y = y +8;
	}		
}
//m^n函数
u32 mypow(u8 m,u8 n)
{
	u32 result=1;	 
	while(n--)result*=m;    
	return result;
}				  
//显示2个数字
//x,y :起点坐标	 
//len :数字的位数
//f_w:字宽
//f_h:字高
//mode:模式	0,填充模式;1,叠加模式
//num:数值(0~4294967295);	 		  
void lcd_showNum(u8 x,u8 y,u32 num,u8 len,u8 f_w,u8 f_h)
{         	
	u8 t,temp;
	u8 enshow=0;						   
	for(t=0;t<len;t++)
	{
		temp=(num/mypow(10,len-t-1))%10;
		if(enshow==0&&t<(len-1))
		{
			if(temp==0)
			{
				lcd_showChar(x+(f_w)*t,y,' ',f_w,f_h,1);
				continue;
			}
			else 
				enshow=1; 
		}
	 	lcd_showChar(x+(f_w)*t,y,temp+'0',f_w,f_h,1); 
	}
} 
//显示字符串
//x,y:起点坐标  
//f_w:字宽
//f_h:字高
//*p:字符串起始地址 
void lcd_showString(u8 x,u8 y,const u8 *p,u8 f_w,u8 f_h,u8 re)
{	
    while((*p<='~')&&(*p>=' '))//判断是不是非法字符!
    {       
        if(x>(LCD_WIDTH-(f_w))){x=0;y+=f_h;}
        if(y>(LCD_HIGH-f_h)){y=x=0;lcd_clear();}
        lcd_showChar(x,y,*p,f_w,f_h,re);	 
        x+=f_w;
        p++;
    }  
	
}
