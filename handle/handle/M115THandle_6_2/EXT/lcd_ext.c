//写指令到 LCD 模块
void transfer_command(uint8_t data)
{
	uint8_t i;
	LCD_CS_OFF();
	LCD_RS_OFF();
	for(i=0;i<8;i++)
	{
		LCD_CLK_OFF();
		if(data&0x80) 
			LCD_DAT_ON();
		else 
			LCD_DAT_OFF();
		LCD_CLK_ON();
		data<<=1;
	}
	LCD_CS_ON();
}

//写数据到 LCD 模块
void transfer_data(uint8_t data)
{
	uint8_t i;
	LCD_CS_OFF();
	LCD_RS_ON();
	for(i=0;i<8;i++)
	{
		LCD_CLK_OFF();
		if(data&0x80) 
			LCD_DAT_ON();
		else 
			LCD_DAT_OFF();
		LCD_CLK_ON();
		data<<=1;
	}
	LCD_CS_ON();
}


